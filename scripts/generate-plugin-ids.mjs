#!/usr/bin/env node
// Scans extensions/{name}/openclaw.plugin.json and generates a TypeScript
// module exporting the set of our plugin IDs. Run as part of the build
// so ToolCapabilityModel doesn't need a hardcoded list.
import { readdirSync, readFileSync, existsSync, writeFileSync, mkdirSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, "..");
const outputDir = join(repoRoot, "apps/desktop/src/generated");
const outputPath = join(outputDir, "our-plugin-ids.ts");

// Scan both public and private extension directories.
const extensionDirs = [
  join(repoRoot, "extensions"),
  join(repoRoot, "extensions-merchant"),
];

const pluginIds = [];

for (const extensionsDir of extensionDirs) {
  if (!existsSync(extensionsDir)) continue;
  for (const entry of readdirSync(extensionsDir, { withFileTypes: true })) {
    if (!entry.isDirectory()) continue;
    const manifestPath = join(extensionsDir, entry.name, "openclaw.plugin.json");
    if (!existsSync(manifestPath)) continue;
    const manifest = JSON.parse(readFileSync(manifestPath, "utf-8"));
    if (manifest.id) pluginIds.push(manifest.id);
  }
}

pluginIds.sort();

const content = `// Auto-generated by scripts/generate-plugin-ids.mjs — do not edit manually
export const OUR_PLUGIN_IDS: ReadonlySet<string> = new Set(${JSON.stringify(pluginIds, null, 2)});
`;

mkdirSync(outputDir, { recursive: true });
writeFileSync(outputPath, content, "utf-8");
console.log(`Generated ${outputPath} with ${pluginIds.length} plugin IDs`);
